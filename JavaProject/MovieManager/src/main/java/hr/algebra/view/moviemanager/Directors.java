    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!formValid()) {
            return;
        }

        int directorId;

        try {
            Director director = new Director(
                    tfFirstName.getText().trim(),
                    tfLastName.getText().trim()
            );

            directorId = directorRepository.createDirector(director);
            directorTableModel.setDirectors(directorRepository.selectDirectors());

            if (directorId > 0) {
                MessageUtils.showInformationMessage("Succesful", "Director was added succesfuly");
            } else {
                MessageUtils.showErrorMessage("Failed", "Director adding failed. Please try again.");
            }

            clearForm();
            directorTableModel.fireTableDataChanged();
        } catch (Exception ex) {
            Logger.getLogger(Directors.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedDirector == null) {
            MessageUtils.showInformationMessage("Not selected actor", "Please select actor to update");
            return;
        }

        int directorId = selectedDirector.getId();
        selectedDirector.setFirstName(tfFirstName.getText().trim());
        selectedDirector.setLastName(tfLastName.getText().trim());

        try {
            directorRepository.updateDirector(directorId, selectedDirector);
            directorTableModel.setDirectors(directorRepository.selectDirectors());

            if (directorId > 0) {
                MessageUtils.showInformationMessage("Succesful", "Director was updated succesfuly");
            } else {
                MessageUtils.showErrorMessage("Failed", "Director updated failed. Please try again.");
            }

            clearForm();
            directorTableModel.fireTableDataChanged();

        } catch (Exception ex) {
            Logger.getLogger(Directors.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedDirector == null) {
            MessageUtils.showInformationMessage("Not selected director", "Please select director to delete");
        }

        try {
            int directorId = selectedDirector.getId();
            directorRepository.deleteDirector(directorId);
            directorTableModel.setDirectors(directorRepository.selectDirectors());

            if (directorId > 0) {
                MessageUtils.showInformationMessage("Succesful", "Director was deleted succesfuly");

            } else {
                MessageUtils.showErrorMessage("Failed", "Director delete failed. Please try again.");
            }

            clearForm();
            directorTableModel.fireTableDataChanged();

        } catch (Exception ex) {
            Logger.getLogger(Directors.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

